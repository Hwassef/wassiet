import 'package:dartz/dartz.dart';
import 'package:wassiet/app/domain/entities/country.dart';
import 'package:wassiet/app/domain/repositories/wassiet_repository.dart';
import 'package:wassiet/app/error/failures.dart';
import 'package:wassiet/app/usecase.dart';

class GetAllCountries extends UseCase<List<Country>, NoParams> {
  final WassietRepository repository;
  GetAllCountries(this.repository);

  @override
  Future<Either<Failure, List<Country>>> call(NoParams params) async {
    return await repository.getAllCountries();
  }
}
